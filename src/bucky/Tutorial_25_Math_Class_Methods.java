package bucky;

public class Tutorial_25_Math_Class_Methods {
	public static void main(String[] args){
		
		System.out.println(Math.abs(-26.7));  //Absolute value gives you the number of how far you are from zero
		
		System.out.println(Math.ceil(7.2));  //Ceil will round whatever decimal number you get up
		
		System.out.println(Math.floor(7.9));  //Floor will round whatever decimal number you get down
		
		System.out.println(Math.max(2.6, 4.3));  //Max takes 2 numbers and gives you the highest of the two. The number are separated by a comma
		
		System.out.println(Math.min(2.6, 4.3));  //Min takes 2 numbers and gives you the lowest of the two. The numbers are separated by a comma
		
		System.out.println(Math.pow(5, 3));  //Pow is the power function and it takes the 1st # and raises it by the 2nd #
		
		System.out.println(Math.sqrt(9));  //Sqrt is the square root function and it give you the square of one number
		
	}

}
